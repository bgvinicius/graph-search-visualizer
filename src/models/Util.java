package models;

import java.util.*;

public class Util {
    public static Object[] shuffle(Object[] array) {
        List<Obect[]> list = new ArrayList<>();
        
        for (int i = 0; i < array.length; i++) {
            list.add(array[i]);
        }

        Collections.shuffle(list);

        for (int i = 0; i < list.size(); i++) {
            array[0] = list.get(i);
        }
    }
}